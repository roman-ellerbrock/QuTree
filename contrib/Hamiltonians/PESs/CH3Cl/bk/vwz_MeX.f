C%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
C% SUBROUTINE VWZPREC(...)
C% 
C% Precalculates Monomials for model.f, that should save some time.
C% 
C% Variables:
C% n: Number of Point (int)
C% q: Coordinates of the Point (q(1:3)=(a,a,a),q(1:6)=(ex,ey,ex,ey,ex,ey)
C%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
      subroutine vwzprec(n,q)
      implicit none

      include 'params.incl'
      include 'states.incl'
      include 'vwz_MeX.incl'

      integer n !Nummer des Punktes

      double precision q(qn) !Koordinaten-Vektor

      double precision a,b,c             !a1-moden
      double precision x4,x5,x6,y4,y5,y6 !e-Moden

      integer i

      !a1-moden
      a =q(1)
      b =q(2)
      c =q(3)
      !e-moden
      x4=q(4)
      y4=q(5)
      x5=q(6)
      y5=q(7)
      x6=q(8)
      y6=q(9)

      sokoord(1,n) = a
      sokoord(2,n) = x4
      sokoord(3,n) = y4
      sokoord(4,n) = b
      sokoord(5,n) = x5
      sokoord(6,n) = y5
      sokoord(7,n) = c
      sokoord(8,n) = x6
      sokoord(9,n) = y6

!****************************************************************************
      !1. a1-mode:
      pa(1,n) = a
      pa(2,n) = a**2
      pa(3,n) = a**3
      pa(4,n) = a**4
      pa(5,n) = a**5
      pa(6,n) = a**6
      pa(7,n) = a**7
      pa(8,n) = a**8

!****************************************************************************
      !2. a1-mode:
      pb(1,n) = b
      pb(2,n) = b**2
      pb(3,n) = b**3
      pb(4,n) = b**4
      pb(5,n) = b**5
      pb(6,n) = b**6
      pb(7,n) = b**7
      pb(8,n) = b**8

!****************************************************************************
      !3. a1-mode
      pc(1,n) = c
      pc(2,n) = c**2
      pc(3,n) = c**3
      pc(4,n) = c**4
      pc(5,n) = c**5
      pc(6,n) = c**6
      pc(7,n) = c**7
      pc(8,n) = c**8

!****************************************************************************
!..   compute vjt terms for first e set:
      va(1,n) = x4**2 + y4**2
      va(2,n) = 2.d0 * x4**3 - 6.d0 * x4 * y4**2
      va(3,n) = x4**4 + 2.d0 * x4**2 * y4**2 + y4**4
      va(4,n) = 2.d0 * x4**5 - 4.d0 * x4**3 * y4**2 - 6.d0 * x4 * y4**4
      va(5,n) = 2.d0 * x4**6 - 30.d0*x4**4*y4**2 + 30.d0*x4**2*y4**4
     &   -2.d0 * y4**6
      va(6,n) = x4**6 + 3.d0 * x4**4 * y4**2 + 3.d0 * x4**2 * y4**4
     &   + y4**6

!****************************************************************************
!..   compute vjt terms for second e set:
      vb(1,n) = x5**2 + y5**2
      vb(2,n) = 2.d0 * x5**3 - 6.d0 * x5 * y5**2
      vb(3,n) = x5**4 + 2.d0 * x5**2 * y5**2 + y5**4
      vb(4,n) = 2.d0 * x5**5 - 4.d0 * x5**3 * y5**2 - 6.d0 * x5 * y5**4
      vb(5,n) = 2.d0 * x5**6 - 30.d0*x5**4*y5**2 + 30.d0*x5**2*y5**4
     &   -2.d0 * y5**6
      vb(6,n) = x5**6 + 3.d0 * x5**4 * y5**2 + 3.d0 * x5**2 * y5**4
     &   + y5**6

!****************************************************************************
!..   compute vjt terms for third e set:
      vc(1,n) = x6**2 + y6**2
      vc(2,n) = 2.d0 * x6**3 - 6.d0 * x6 * y6**2
      vc(3,n) = x6**4 + 2.d0 * x6**2 * y6**2 + y6**4
      vc(4,n) = 2.d0 * x6**5 - 4.d0 * x6**3 * y6**2 - 6.d0 * x6 * y6**4
      vc(5,n) = 2.d0 * x6**6 - 30.d0*x6**4*y6**2 + 30.d0*x6**2*y6**4
     &   -2.d0 * y6**6
      vc(6,n) = x6**6 + 3.d0 * x6**4 * y6**2 + 3.d0 * x6**2 * y6**4
     &   + y6**6

!****************************************************************************
!..   compute wjt terms for first e set:
      wa(1,n) = x4
      wa(2,n) = x4**2 - y4**2
      wa(3,n) = x4**3 + x4*y4**2
      wa(4,n) = x4**4 - 6.d0 * x4**2 * y4**2 + y4**4
      wa(5,n) = x4**4 - y4**4
      wa(6,n) = x4**5 - 10.d0 * x4**3 * y4**2 + 5.d0 * x4 * y4**4
      wa(7,n) = x4**5 + 2.d0 * x4**3 * y4**2 + x4 * y4**4
      wa(8,n) = x4**6 - 5.d0 * x4**4 * y4**2 - 5.d0 * x4**2*y4**4+y4**6
      wa(9,n) = x4**6 + x4**4 * y4**2 - x4**2 * y4**4 - y4**6

!****************************************************************************
!..   compute wjt terms for second e set:
      wb(1,n) = x5
      wb(2,n) = x5**2 - y5**2
      wb(3,n) = x5**3 + x5*y5**2
      wb(4,n) = x5**4 - 6.d0 * x5**2 * y5**2 + y5**4
      wb(5,n) = x5**4 - y5**4
      wb(6,n) = x5**5 - 10.d0 * x5**3 * y5**2 + 5.d0 * x5 * y5**4
      wb(7,n) = x5**5 + 2.d0 * x5**3 * y5**2 + x5 * y5**4
      wb(8,n) = x5**6 - 5.d0 * x5**4 * y5**2 - 5.d0 * x5**2*y5**4+y5**6
      wb(9,n) = x5**6 + x5**4 * y5**2 - x5**2 * y5**4 - y5**6

!****************************************************************************
!..   compute wjt terms for third e set:
      wc(1,n) = x6
      wc(2,n) = x6**2 - y6**2
      wc(3,n) = x6**3 + x6*y6**2
      wc(4,n) = x6**4 - 6.d0 * x6**2 * y6**2 + y6**4
      wc(5,n) = x6**4 - y6**4
      wc(6,n) = x6**5 - 10.d0 * x6**3 * y6**2 + 5.d0 * x6 * y6**4
      wc(7,n) = x6**5 + 2.d0 * x6**3 * y6**2 + x6 * y6**4
      wc(8,n) = x6**6 - 5.d0 * x6**4 * y6**2 - 5.d0 * x6**2*y6**4+y6**6
      wc(9,n) = x6**6 + x6**4 * y6**2 - x6**2 * y6**4 - y6**6

!****************************************************************************
!..   compute zjt terms for first e set:
      za(1,n) = y4
      za(2,n) = -2.d0*x4*y4
      za(3,n) = y4**3 + y4*x4**2
      za(4,n) = 4.d0*x4**3*y4 - 4.d0*x4*y4**3
      za(5,n) = -2.d0*x4**3*y4 - 2.d0*x4*y4**3
      za(6,n) = -5.d0*x4**4*y4 + 10.d0*x4**2*y4**3 - y4**5
      za(7,n) =  x4**4*y4 + 2.d0*x4**2*y4**3 + y4**5
      za(8,n) = 4.d0*x4**5*y4 - 4.d0*x4*y4**5
      za(9,n) = -2.d0*x4**5*y4 - 4.d0*x4**3*y4**3 - 2.d0*x4*y4**5

!****************************************************************************
!..   compute zjt terms for second e set:
      zb(1,n) = y5
      zb(2,n) = -2.d0*x5*y5
      zb(3,n) = y5**3 + y5*x5**2
      zb(4,n) = 4.d0*x5**3*y5 - 4.d0*x5*y5**3
      zb(5,n) = -2.d0*x5**3*y5 - 2.d0*x5*y5**3
      zb(6,n) = -5.d0*x5**4*y5 + 10.d0*x5**2*y5**3 - y5**5
      zb(7,n) =  x5**4*y5 + 2.d0*x5**2*y5**3 + y5**5
      zb(8,n) = 4.d0*x5**5*y5 - 4.d0*x5*y5**5
      zb(9,n) = -2.d0*x5**5*y5 - 4.d0*x5**3*y5**3 - 2.d0*x5*y5**5

!****************************************************************************
!..   compute zjt terms for third e set:
      zc(1,n) = y6
      zc(2,n) = -2.d0*x6*y6
      zc(3,n) = y6**3 + y6*x6**2
      zc(4,n) = 4.d0*x6**3*y6 - 4.d0*x6*y6**3
      zc(5,n) = -2.d0*x6**3*y6 - 2.d0*x6*y6**3
      zc(6,n) = -5.d0*x6**4*y6 + 10.d0*x6**2*y6**3 - y6**5
      zc(7,n) =  x6**4*y6 + 2.d0*x6**2*y6**3 + y6**5
      zc(8,n) = 4.d0*x6**5*y6 - 4.d0*x6*y6**5
      zc(9,n) = -2.d0*x6**5*y6 - 4.d0*x6**3*y6**3 - 2.d0*x6*y6**5

!****************************************************************************
!..   compute V_eeab terms:
      veeab(1,n) =  2.d0*x4*x5 + 2.d0*y4*y5
      veeab(2,n) = 2.d0*x4*x5**2 - 2.d0*x4*y5**2 - 4.d0*x5*y4*y5
      veeab(3,n) = 2.d0*x4**2*x5 - 2.d0*x5*y4**2 - 4.d0*x4*y4*y5
      veeab(4,n) = 2.d0*x4**2*x5**2 - 2.d0*x4**2*y5**2
     &          - 2.d0*x5**2*y4**2 + 2.d0*y4**2*y5**2
     &          + 8.d0*x4*x5*y4*y5
      veeab(5,n) = 2.d0*x4**3*x5 + 2.d0*x4**2*y4*y5
     &          + 2.d0*x4*x5*y4**2 + 2.d0*y4**3*y5
      veeab(6,n) = 2.d0*x5**3*x4 + 2.d0*x4*x5*y5**2
     &         + 2.d0*x5**2*y4*y5 + 2.d0*y5**3*y4
      veeab(7,n) = x4**2*x5**2 + x4**2*y5**2
     &          + x5**2*y4**2 + y4**2*y5**2

      veeab(8,n) = x4*y5**4-4*x5*y4*y5**3-6*x4*x5**2*y5**2+4*x5**3*y4*y5
     &          +x4*x5**4
      veeab(9,n) = (y5**2+x5**2)*(x4*y5**2+2*x5*y4*y5-x4*x5**2)
      veeab(10,n) = (y4**2+x4**2)*(x4*y5**2+2*x5*y4*y5-x4*x5**2)
      veeab(11,n) = x5*(y4**2+x4**2)*(3.d0*y5**2-x5**2)
      veeab(12,n) = (2.d0*x4*y4*y5+x5*y4**2-x4**2*x5)*(y5**2+x5**2)
      veeab(13,n) = x4*(3.d0*y4**2-x4**2)*(y5**2+x5**2)
      veeab(14,n) = 4.d0*x4*y4**3*y5-4.d0*x4**3*y4*y5-x5*y4**4
     &          +6.d0*x4**2*x5*y4**2-x4**4*x5
      veeab(15,n) = (y4**2+x4**2)*(2.d0*x4*y4*y5+x5*y4**2-x4**2*x5)


      veeab(16,n) = (y4*y5**5-5.d0*x4*x5*y5**4-10.d0*x5**2*y4*y5**3
     &          +10.d0*x4*x5**3*y5**2+5.d0*x5**4*y4*y5-x4*x5**5)
      veeab(17,n) = (y4*y5**2-x4*y5**2-2.d0*x5*y4*y5-2.d0*x4*x5*y5
     &          -x5**2*y4+x4*x5**2)*(y4*y5**2+x4*y5**2+2.d0*x5*y4*y5
     &          -2.d0*x4*x5*y5-x5**2*y4-x4*x5**2)
      veeab(18,n) = (y4*y5-x4*x5)*(y4**2*y5**2-3.d0*x4**2*y5**2
     &          -8.d0*x4*x5*y4*y5-3.d0*x5**2*y4**2+x4**2*x5**2)
      veeab(19,n) = (y4**2*y5-2.d0*x4*y4*y5-x4**2*y5-x5*y4**2
     &          -2.d0*x4*x5*y4+x4**2*x5)*(y4**2*y5+2.d0*x4*y4*y5
     &          -x4**2*y5+x5*y4**2-2.d0*x4*x5*y4-x4**2*x5)
      veeab(20,n) = (y4**5*y5-10.d0*x4**2*y4**3*y5+5.d0*x4**4*y4*y5
     &          -5.d0*x4*x5*y4**4+10.d0*x4**3*x5*y4**2-x4**5*x5)

      veeab(21,n) = (y4*y5+x4*x5)*(y5**2+x5**2)**2
      veeab(22,n) = (y4*y5-x4*y5+x5*y4+x4*x5)*(y4*y5+x4*y5
     &          -x5*y4+x4*x5)*(y5**2+x5**2)
      veeab(23,n) = (y4**2+x4**2)*(y5**2+x5**2)**2
      veeab(24,n) = (y4*y5+x4*x5)*(y4**2*y5**2-3.d0*x4**2*y5**2
     &          +8.d0*x4*x5*y4*y5-3.d0*x5**2*y4**2+x4**2*x5**2)
      veeab(25,n) = (y4**2+x4**2)*(y4*y5+x4*x5)*(y5**2+x5**2)
      veeab(26,n) = (y4**2+x4**2)*(y4*y5-x4*y5+x5*y4+x4*x5)
     &          *(y4*y5+x4*y5-x5*y4+x4*x5)
      veeab(27,n) = (y4**2+x4**2)**2*(y5**2+x5**2)
      veeab(28,n) = (y4**2+x4**2)**2*(y4*y5+x4*x5)

!****************************************************************************
!..   compute Wcoup_eeab terms:
      weeab(1,n) = x4 * x5 - y4 * y5
      weeab(2,n) = x4**2 * x5 + x5 * y4**2
      weeab(3,n) = x4 * y5**2 + x4 * x5**2
      weeab(4,n) = 2.d0 * x4 * y4 * y5 + x4**2 * x5 - x5 * y4**2
      weeab(5,n) = x4 * x5**2 + 2.d0 * x5 * y4*y5 - x4 * y5**2
      weeab(6,n) = x4**3 * x5 - 3.d0 * x4**2 *y4*y5
     &     - 3.d0*x4*x5*y4**2 + y4**3*y5
      weeab(7,n) = x4**2*x5**2 - x4**2*y5**2 - x5**2*y4**2 + y4**2*y5**2
     &     - 4.d0*x4*x5*y4*y5
      weeab(8,n) =x5**3*x4 - 3.d0*x4*x5*y5**2
     &     - 3.d0*x5**2*y4*y5+y5**3*y4
!               weeab44(9,n) =x4**3*x5 - 3.d0*x4**2*y4*y5 -3.d0*x4*x5*y4**2-y4**3*y5
      weeab(9,n) =x4**3*x5 + 3.d0*x4**2*y4*y5
     &     - 3.d0*x4*x5*y4**2-y4**3*y5
!               weeab45(10,n) =x5**3*x4 - 3.d0*x4*x5*y5**2 - 3.d0*x5**2*y4*y5-y5**3*y4
      weeab(10,n) =x5**3*x4 - 3.d0*x4*x5*y5**2
     &     + 3.d0*x5**2*y4*y5-y5**3*y4
      weeab(11,n) = x4**3*x5 - x4**2*y4*y5 + x4*x5*y4**2 - y4**3*y5
      weeab(12,n) = x4**2*x5**2 + x4**2*y5**2 - x5**2*y4**2
     &     - y4**2*y5**2
      weeab(13,n) = x4**2*x5**2 - x4**2*y5**2 + x5**2*y4**2
     &     - y4**2*y5**2
      weeab(14,n) = x4*x5**3 + x4*x5*y5**2 - x5**2*y4*y5 - y5**3*y4

      weeab(15,n) = (x4*y5**4+4.d0*x5*y4*y5**3-6.d0*x4*x5**2*y5**2
     &          -4.d0*x5**3*y4*y5+x4*x5**4)
      weeab(16,n) = (2.d0*x4*y4*y5**3+3.d0*x5*y4**2*y5**2
     & -3.d0*x4**2*x5*y5**2-6.d0*x4*x5**2*y4*y5-x5**3*y4**2+x4**2*x5**3)
      weeab(17,n) = (3.d0*x4*y4**2*y5**2-x4**3*y5**2+2.d0*x5*y4**3*y5
     &          -6.d0*x4**2*x5*y4*y5-3.d0*x4*x5**2*y4**2+x4**3*x5**2)
      weeab(18,n) = (4.d0*x4*y4**3*y5-4.d0*x4**3*y4*y5+x5*y4**4
     &          -6.d0*x4**2*x5*y4**2+x4**4*x5)

      weeab(19,n) = -(y5**2+x5**2)*(x4*y5**2-2.d0*x5*y4*y5-x4*x5**2)
      weeab(20,n) = x4*(y5**2+x5**2)**2
      weeab(21,n) = -(2.d0*x4*y4*y5**3-3.d0*x5*y4**2*y5**2+3.d0*x4**2
     &          *x5*y5**2-6.d0*x4*x5**2*y4*y5+x5**3*y4**2-x4**2*x5**3)
      weeab(22,n) = x5*(y4**2+x4**2)*(y5**2+x5**2)
      weeab(23,n) = -(y4**2+x4**2)*(x4*y5**2-2.d0*x5*y4*y5-x4*x5**2)
      weeab(24,n) = x4*(y4**2+x4**2)*(y5**2+x5**2)
      weeab(25,n) = x5*(y4**2+x4**2)**2
      weeab(26,n) = (y4**2+x4**2)*(2.d0*x4*y4*y5-x5*y4**2+x4**2*x5)

      weeab(27,n) = (y4*y5**5+5.d0*x4*x5*y5**4-10.d0*x5**2*y4*y5**3
     &    -10.d0*x4*x5**3*y5**2+5.d0*x5**4*y4*y5+x4*x5**5)
      weeab(28,n) = (y5**2+x5**2)*(y4*y5**3-3.d0*x4*x5*y5**2
     &    -3.d0*x5**2*y4*y5+x4*x5**3)
      weeab(29,n) = (y4**2+x4**2)*(y5**2-2.d0*x5*y5-x5**2)
     &    *(y5**2+2.d0*x5*y5-x5**2)
      weeab(30,n) = (y4*y5-x4*y5-x5*y4-x4*x5)*(y4*y5+x4*y5
     &    +x5*y4-x4*x5)*(y5**2+x5**2)
      weeab(31,n) = (y4**2+x4**2)*(y4*y5**3-3.d0*x4*x5*y5**2
     &    -3.d0*x5**2*y4*y5+x4*x5**3)
      weeab(32,n) = (y4**3*y5-3.d0*x4**2*y4*y5
     &     -3.d0*x4*x5*y4**2+x4**3*x5)*(y5**2+x5**2)
      weeab(33,n) = (y4**2+x4**2)*(y4*y5-x4*y5-x5*y4-x4*x5)
     &    *(y4*y5+x4*y5+x5*y4-x4*x5)
      weeab(34,n) = (y4**2-2.d0*x4*y4-x4**2)*(y4**2+2.d0*x4*y4-x4**2) 
     &    *(y5**2+x5**2)
      weeab(35,n) = (y4**2+x4**2)*(y4**3*y5-3.d0*x4**2*y4*y5
     &    -3.d0*x4*x5*y4**2+x4**3*x5)
      weeab(36,n)= (y4**5*y5-10.d0*x4**2*y4**3*y5+5.d0*x4**4*y4*y5
     &    +5.d0*x4*x5*y4**4-10.d0*x4**3*x5*y4**2+x4**5*x5)

      weeab(37,n) = (y4*y5-x4*x5)*(y5**2+x5**2)**2
      weeab(38,n) = (y5**2+x5**2)*(y4*y5**3+3.d0*x4*x5*y5**2
     &    -3.d0*x5**2*y4*y5-x4*x5**3)
      weeab(39,n) = (y4**2+x4**2)*(y5-x5)*(y5+x5)*(y5**2+x5**2)
      weeab(40,n) = (y4-x4)*(y4+x4)*(y5**2+x5**2)**2
      weeab(41,n) = (y4*y5**2-x4*y5**2+2.d0*x5*y4*y5+2.d0*x4*x5*y5
     &    -x5**2*y4+x4*x5**2)*(y4*y5**2+x4*y5**2-2.d0*x5*y4*y5
     &    +2.d0*x4*x5*y5-x5**2*y4-x4*x5**2)
